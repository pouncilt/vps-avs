package gov.va.med.lom.avs.client.model;

public class StringResourceJson  {

  private String stationNo;
  private String languageAbbr;
  private String languageName;
  private String name;
  private String value;
  
  public String getStationNo() {
    return stationNo;
  }
  public void setStationNo(String stationNo) {
    this.stationNo = stationNo;
  }
  public String getValue() {
    return value;
  }
  public void setValue(String value) {
    this.value = value;
  }
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public String getLanguageAbbr() {
    return languageAbbr;
  }
  public void setLanguageAbbr(String languageAbbr) {
    this.languageAbbr = languageAbbr;
  }
  public String getLanguageName() {
    return languageName;
  }
  public void setLanguageName(String languageName) {
    this.languageName = languageName;
  }
 
}
